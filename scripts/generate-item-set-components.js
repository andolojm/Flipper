#!/usr/bin/env node
/**
 * For each item set ID in src/data/item-set-ids.json, fetches the OSRS Wiki
 * page, extracts component item names from {{CostLine|...}} templates, resolves
 * them to item IDs via the prices mapping API, and writes the result to
 * src/data/itemSets.ts.
 *
 * Run generate:item-set-ids first if the set list needs refreshing.
 *
 * Usage:
 *   node scripts/generate-item-set-components.js
 *   # or via package.json:
 *   npm run generate:item-set-components
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_FILE = resolve(__dirname, '../src/data/itemSets.ts');
const SET_IDS_FILE = resolve(__dirname, '../src/data/item-set-ids.json');

const MAPPING_URL = 'https://prices.runescape.wiki/api/v1/osrs/mapping';
const WIKI_API = 'https://oldschool.runescape.wiki/api.php';
const HEADERS = { 'User-Agent': 'flipperino-item-set-scraper/1.0' };

const SET_IDS = JSON.parse(readFileSync(SET_IDS_FILE, 'utf8'));

// --- 1. Fetch prices mapping ---
console.log('Fetching prices mapping…');
const mappingRes = await fetch(MAPPING_URL, { headers: HEADERS });
if (!mappingRes.ok) throw new Error(`Mapping fetch failed: HTTP ${mappingRes.status}`);
const mapping = await mappingRes.json();

const idToName = new Map(mapping.map((item) => [item.id, item.name]));
// Case-insensitive name → id lookup
const nameToId = new Map(mapping.map((item) => [item.name.toLowerCase(), item.id]));
console.log(`Mapping loaded: ${idToName.size} items`);

// --- 2. Fetch wikitext for a set page and extract component names ---
async function fetchComponents(setId) {
  const setName = idToName.get(setId);
  if (!setName) {
    console.warn(`  [${setId}] Not found in mapping, skipping`);
    return [];
  }

  const title = setName.replace(/ /g, '_');
  const url =
    `${WIKI_API}?action=query&titles=${encodeURIComponent(title)}` +
    `&prop=revisions&rvprop=content&format=json`;

  const res = await fetch(url, { headers: HEADERS });
  if (!res.ok) throw new Error(`Wiki fetch failed for ${title}: HTTP ${res.status}`);

  const json = await res.json();
  const pages = json?.query?.pages ?? {};
  const page = Object.values(pages)[0];
  const wikitext = page?.revisions?.[0]?.['*'] ?? '';

  if (!wikitext) {
    console.warn(`  [${setId}] ${setName}: empty wikitext`);
    return [];
  }

  // Extract item names from {{CostLine|Item name}} templates
  const componentNames = [...wikitext.matchAll(/\{\{CostLine\|([^}|]+)/gi)].map((m) =>
    m[1].trim()
  );

  // Resolve names to IDs
  const componentIds = componentNames
    .map((name) => nameToId.get(name.toLowerCase()))
    .filter((id) => id !== undefined);

  const unresolved = componentNames.filter((n) => !nameToId.has(n.toLowerCase()));
  if (unresolved.length) {
    console.warn(`  [${setId}] ${setName}: unresolved names: ${unresolved.join(', ')}`);
  }

  return [...new Set(componentIds)];
}

// --- 3. Process all sets with a concurrency limit ---
const CONCURRENCY = 10;

async function processInBatches(ids) {
  const results = new Map();
  for (let i = 0; i < ids.length; i += CONCURRENCY) {
    const batch = ids.slice(i, i + CONCURRENCY);
    const settled = await Promise.allSettled(
      batch.map(async (id) => {
        const components = await fetchComponents(id);
        return { id, components };
      })
    );
    for (const result of settled) {
      if (result.status === 'fulfilled') {
        results.set(result.value.id, result.value.components);
      } else {
        console.error(`  Batch error: ${result.reason}`);
      }
    }
    console.log(`  Processed ${Math.min(i + CONCURRENCY, ids.length)}/${ids.length}`);
  }
  return results;
}

console.log(`\nFetching components for ${SET_IDS.length} item sets…`);
const componentMap = await processInBatches(SET_IDS);

// --- 4. Write src/data/itemSets.ts ---
const lines = SET_IDS.map((id) => {
  const name = idToName.get(id) ?? `ID ${id}`;
  const components = componentMap.get(id) ?? [];
  return `  ${id}: [${components.join(', ')}], // ${name}`;
});

const output = [
  '/**',
  ' * Maps each item set ID to its component item IDs.',
  ' * Generated by scripts/generate-item-set-components.js — do not edit manually.',
  ' */',
  'export const ITEM_SETS: Record<number, number[]> = {',
  ...lines,
  '};',
  '',
  '/** Derived set of all item set IDs for fast membership checks */',
  'export const ITEM_SET_IDS = new Set(Object.keys(ITEM_SETS).map(Number));',
  '',
].join('\n');

writeFileSync(OUT_FILE, output, 'utf8');

const populated = [...componentMap.values()].filter((c) => c.length > 0).length;
console.log(`\nDone. ${populated}/${SET_IDS.length} sets have components.`);
console.log(`Written to ${OUT_FILE}`);
