#!/usr/bin/env node
/**
 * Fetches the rendered HTML of https://oldschool.runescape.wiki/w/Item_IDs via the
 * MediaWiki parse API and extracts every item ID → wiki page URL mapping.
 *
 * Writes the result to src/data/itemWikiUrls.ts.
 *
 * Usage:
 *   node scripts/generate-item-wiki-urls.js
 *   # or via package.json:
 *   npm run generate:item-wiki-urls
 */

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_FILE = resolve(__dirname, '../src/data/itemWikiUrls.ts');

const WIKI_API_URL =
  'https://oldschool.runescape.wiki/api.php' +
  '?action=parse&page=Item_IDs&prop=text&format=json&disablelimitreport=1';
const MAPPING_URL = 'https://prices.runescape.wiki/api/v1/osrs/mapping';

const WIKI_BASE = 'https://oldschool.runescape.wiki';

console.log('Fetching prices mapping and Item_IDs page in parallel…');

const [mappingRes, wikiRes] = await Promise.all([
  fetch(MAPPING_URL, { headers: { 'User-Agent': 'flipperino-item-url-scraper/1.0' } }),
  fetch(WIKI_API_URL, { headers: { 'User-Agent': 'flipperino-item-url-scraper/1.0' } }),
]);

if (!mappingRes.ok) throw new Error(`Mapping fetch failed: HTTP ${mappingRes.status}`);
if (!wikiRes.ok) throw new Error(`Wiki fetch failed: HTTP ${wikiRes.status}`);

const mapping = await mappingRes.json();
const tradableIds = new Set(mapping.map((item) => item.id));
console.log(`Mapping loaded: ${tradableIds.size} tradable items`);

const json = await wikiRes.json();
const html = json?.parse?.text?.['*'];
if (!html) throw new Error('Unexpected API response shape');

console.log(`Received ${(html.length / 1024 / 1024).toFixed(1)} MB of HTML`);

// Each table row looks like (attributes omitted for clarity):
//   <tr>
//     <td><a href="/w/Item_Name">Item Name</a></td>
//     <td><a href="...?type=item&amp;id=12345">12345</a></td>
//   </tr>
// Notes:
//   - href values are URL-encoded (/w/%27name%27)
//   - The ID lookup URL uses HTML entities (&amp;id=) not raw &
//   - Some rows have multiple ID cells (variant items sharing a page)

/** @type {Map<number, string>} */
const entries = new Map();

// Match every <tr> block (no attributes on <tr> in this table)
const rowRe = /<tr>([\s\S]*?)<\/tr>/g;
// Extract href from first <a> in the row (the item page link)
const hrefRe = /<a\s+href="([^"]+)"/;
// Match id= in both raw (?id= / &id=) and HTML-encoded (&amp;id=) forms
const idRe = /(?:[?&]|&amp;)id=(\d+)/g;

let rowMatch;
while ((rowMatch = rowRe.exec(html)) !== null) {
  const row = rowMatch[1];

  const hrefMatch = hrefRe.exec(row);
  if (!hrefMatch) continue;

  // Decode URL-encoding and strip anchor fragments; skip non-item paths
  const rawPath = hrefMatch[1].split('#')[0];
  if (!rawPath.startsWith('/w/')) continue;
  const url = WIKI_BASE + decodeURIComponent(rawPath);

  let idMatch;
  while ((idMatch = idRe.exec(row)) !== null) {
    const id = parseInt(idMatch[1], 10);
    if (tradableIds.has(id) && !entries.has(id)) {
      entries.set(id, url);
    }
  }
}

// Sort by ID for a stable, readable file
const sorted = [...entries.entries()].sort(([a], [b]) => a - b);

console.log(`Extracted ${sorted.length} tradable item ID → URL mappings (${tradableIds.size - sorted.length} tradable IDs had no wiki page)`);

const lines = sorted.map(([id, url]) => `  ${id}: "${url}",`);

const output = [
  '/** Maps item ID to its OSRS Wiki page URL */',
  '/** Generated by scripts/generate-item-wiki-urls.js — do not edit manually */',
  'export const ITEM_WIKI_URLS: Record<number, string> = {',
  ...lines,
  '};',
  '',
].join('\n');

writeFileSync(OUT_FILE, output, 'utf8');
console.log(`Written to ${OUT_FILE}`);
