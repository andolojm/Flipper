// Generates src/data/poisonableItems.ts
// Scrapes the Weapon_poison(++) wiki page for poisonable items,
// then resolves base/poisoned item IDs from the prices mapping API.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_FILE = resolve(__dirname, '../src/data/poisonableItems.ts');

const MAPPING_URL = 'https://prices.runescape.wiki/api/v1/osrs/mapping';
const WIKI_API_URL =
  'https://oldschool.runescape.wiki/api.php?action=parse&page=Weapon_poison(%2B%2B)&prop=text&format=json&disablelimitreport=1';
const HEADERS = { 'User-Agent': 'flipperino-poison-scraper/1.0 (github.com/andolojm/Flipper)' };
const WEAPON_POISON_PP_ID = 5940;

console.log('Fetching mapping and wiki page in parallel…');
const [mappingRes, wikiRes] = await Promise.all([
  fetch(MAPPING_URL, { headers: HEADERS }),
  fetch(WIKI_API_URL, { headers: HEADERS }),
]);
const mapping = await mappingRes.json();
const wiki = await wikiRes.json();
const html = wiki.parse.text['*'];

console.log(`Mapping loaded: ${mapping.length} tradeable items`);

const nameToId = new Map(mapping.map((item) => [item.name, item.id]));

// Each products table <tr> has:
//   2nd <td>: <a title="Poisoned Name">…</a> optionally followed by " × N"
//   last <td class="plainlist">: <ul class="products-materials"> with base item as first <li>
const items = [];
for (const trMatch of html.matchAll(/<tr>([\s\S]*?)<\/tr>/g)) {
  const row = trMatch[1];
  if (row.includes('<th')) continue;

  // Second <td> — name link + optional quantity
  const nameTdMatch = row.match(
    /<td><a[^>]+title="([^"]+)"[^>]*>[^<]+<\/a>(?:\s*×\s*(\d+))?<\/td>/
  );
  if (!nameTdMatch) continue;

  const poisonedName = nameTdMatch[1];
  const quantity = nameTdMatch[2] ? parseInt(nameTdMatch[2]) : 1;
  if (poisonedName === 'Weapon poison(++)') continue;

  // First <li> in products-materials that isn't the selected poison
  const materialsMatch = row.match(/<ul class="products-materials">([\s\S]*?)<\/ul>/);
  if (!materialsMatch) continue;
  const baseLiMatch = materialsMatch[1].match(
    /<li(?!\s+class="production-selected")[^>]*>[\s\S]*?title="([^"]+)"/
  );
  if (!baseLiMatch) continue;

  const baseName = baseLiMatch[1];
  if (baseName === 'Weapon poison(++)') continue;

  const baseId = nameToId.get(baseName);
  const poisonedId = nameToId.get(poisonedName);

  if (baseId == null || poisonedId == null) {
    console.log(`  Skipping (not tradeable): ${baseName} → ${poisonedName}`);
    continue;
  }

  items.push({ baseName, baseId, poisonedName, poisonedId, quantity });
}

console.log(`Found ${items.length} tradeable poisonable items`);

const lines = items.map(
  (item) =>
    `  { baseName: ${JSON.stringify(item.baseName)}, baseId: ${item.baseId}, ` +
    `poisonedName: ${JSON.stringify(item.poisonedName)}, poisonedId: ${item.poisonedId}, quantity: ${item.quantity} },`
);

const output = `// Generated by scripts/generate-poisonable-items.js — do not edit manually
// Run: npm run generate:poisonable-items
export const WEAPON_POISON_PP_ID = ${WEAPON_POISON_PP_ID};

export interface PoisonableItem {
  baseName: string;
  baseId: number;
  poisonedName: string;
  poisonedId: number;
  /** Number of items one vial of Weapon Poison++ covers */
  quantity: number;
}

export const POISONABLE_ITEMS: PoisonableItem[] = [
${lines.join('\n')}
];
`;

writeFileSync(OUT_FILE, output);
console.log(`Written to ${OUT_FILE}`);
